diccionario = {'a': 1, 'b':2, 'c':3, 'd':4}

# keys

llaves = diccionario.keys()
print(llaves)

llaves = tuple(diccionario.keys())
print(llaves)


# values

valores = diccionario.values()
print(valores)

valores = tuple(diccionario.values())
print(valores)

#items

elementos = diccionario.items()
print(elementos)

elementos = tuple(diccionario.items())
print(elementos)